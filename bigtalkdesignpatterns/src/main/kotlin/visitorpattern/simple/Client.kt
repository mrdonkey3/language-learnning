class Client {

    companion object {
        @JvmStatic
        fun main(vararg args: String) {
            println("男人成功时，背后多半有一个伟大的女人。")
            println("女人成功时，背后大多有一个不成功的男人。")
            println("男人失败时，闷头喝酒，谁也不用劝。")
            println("女人失败时，眼泪汪汪，谁也劝不了。")
            println("男人恋爱时，凡事不懂也要装懂。")
            println("女人恋爱时，遇事懂也要装作不懂。")
        }
    }

}